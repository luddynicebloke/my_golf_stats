create table if not exists public.pro_player_access (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  player_user_id uuid not null references public.user_profiles(id) on delete cascade,
  pro_user_id uuid not null references public.user_profiles(id) on delete cascade,
  constraint pro_player_access_unique unique (player_user_id, pro_user_id),
  constraint pro_player_access_not_self check (player_user_id <> pro_user_id)
);

create index if not exists pro_player_access_player_idx
  on public.pro_player_access (player_user_id);

create index if not exists pro_player_access_pro_idx
  on public.pro_player_access (pro_user_id);

create table if not exists public.pro_account_requests (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  user_id uuid not null references public.user_profiles(id) on delete cascade,
  email text not null,
  user_name text,
  status text not null default 'pending',
  reviewed_at timestamptz,
  reviewed_by uuid references public.user_profiles(id) on delete set null,
  constraint pro_account_requests_unique_user unique (user_id),
  constraint pro_account_requests_status_check
    check (status in ('pending', 'approved', 'rejected'))
);

create index if not exists pro_account_requests_status_idx
  on public.pro_account_requests (status, created_at);

alter table public.pro_player_access enable row level security;
alter table public.pro_account_requests enable row level security;

drop policy if exists "Players can view their pro access rows" on public.pro_player_access;
create policy "Players can view their pro access rows"
on public.pro_player_access
for select
to authenticated
using (
  auth.uid() = player_user_id
  or auth.uid() = pro_user_id
);

drop policy if exists "Players can insert their pro access rows" on public.pro_player_access;
create policy "Players can insert their pro access rows"
on public.pro_player_access
for insert
to authenticated
with check (auth.uid() = player_user_id);

drop policy if exists "Players can delete their pro access rows" on public.pro_player_access;
create policy "Players can delete their pro access rows"
on public.pro_player_access
for delete
to authenticated
using (auth.uid() = player_user_id);

drop policy if exists "Users can create their own pro account request" on public.pro_account_requests;
create policy "Users can create their own pro account request"
on public.pro_account_requests
for insert
to authenticated
with check (auth.uid() = user_id);

drop policy if exists "Users can view their own pro account request" on public.pro_account_requests;
create policy "Users can view their own pro account request"
on public.pro_account_requests
for select
to authenticated
using (
  auth.uid() = user_id
  or exists (
    select 1
    from public.user_profiles up
    where up.id = auth.uid()
      and up.role = 'admin'
  )
);

drop policy if exists "Users can update their pending pro account request" on public.pro_account_requests;
create policy "Users can update their pending pro account request"
on public.pro_account_requests
for update
to authenticated
using (auth.uid() = user_id and status = 'pending')
with check (auth.uid() = user_id and status = 'pending');

create or replace function public.enforce_max_two_pros()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  if (
    select count(*)
    from public.pro_player_access ppa
    where ppa.player_user_id = new.player_user_id
  ) >= 2 then
    raise exception 'You can only allow up to 2 pros.';
  end if;

  return new;
end;
$$;

drop trigger if exists enforce_max_two_pros_trigger on public.pro_player_access;
create trigger enforce_max_two_pros_trigger
before insert on public.pro_player_access
for each row
execute function public.enforce_max_two_pros();

create or replace function public.can_view_player_data(p_target_user_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select
    case
      when auth.uid() is null or p_target_user_id is null then false
      when auth.uid() = p_target_user_id then true
      when exists (
        select 1
        from public.user_profiles up
        where up.id = auth.uid()
          and up.role = 'admin'
      ) then true
      when exists (
        select 1
        from public.pro_player_access ppa
        join public.user_profiles up on up.id = auth.uid()
        where ppa.player_user_id = p_target_user_id
          and ppa.pro_user_id = auth.uid()
          and up.role = 'pro'
      ) then true
      else false
    end;
$$;

grant execute on function public.can_view_player_data(uuid) to authenticated;

